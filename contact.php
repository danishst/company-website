            function sendMail(name, email, phone, msg, btnId, formId) {
                $.ajax({
                    url: "sendmail.php",
                    method: "POST",
                    data: {
                        name: name,
                        email: email,
                        phone: phone,
                        msg: msg,
                        msg_submit: true,
